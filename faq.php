                                 <div class="panel-heading padding-vertical-10 padding-horizontal-15">
                                     <h4 class="panel-title padding-left-30">  <a data-toggle="collapse" data-parent="#accordion" href="#collapsenine" class="collapsed">Do I need to clean before fogging?</a>  </h4>
                                 </div>
                                 <div id="collapsenine" class="panel-collapse collapse" style="height: 0px;">
                                     <div class="panel-body"> 
                                         
                                         <div class="faq_post padding-left-40">
                                             <div class="post-entry clearfix margin-top-10">  <img src="images/faq-img1.jpg" alt="" class="alignleft" />
                                                 <p>Fogging is the vaporization of a solution into the air in an environment where surface sanitization is a concern. Fogging may be performed in air-ducts, HVAC systems, in labs, equipment and cleanrooms. There are several sanitizing solutions that may be used for fogging. Hydrogen Peroxide (H2O2), familiar to most people, is one such solution. In short, the technology utilizes the free radicals generated from hydrogen peroxide vapor to kill micro-organisms such as bacteria, viruses and fungi. The process relies on kinetic energy to ensure that the vaporized H2O2 superoxide and hydroxyl radicals are able to effectively contact and deactivate the microorganisms. Once the bio-deactivation process has finished the hydrogen peroxide vapor is catalytically converted to water and oxygen, H2O and O2.</p>

<p>Cleaning and physical characteristics of the room must be taken into account when considering fogging as an element of the sanitization process. As mentioned above, fogging is a surface sanitizer. The vapor must make contact with the microorganisms. Fogging does not clean a surface. The surfaces must be cleaned prior to fogging. If there is gross contamination – e.g. dust, debris, or papers, boxes, wet or moist areas or anything covering a surface to be sanitized – then the efficacy of the fogging process will be compromised. It should also be noted that certain bacteria can remain viable in dust and debris for long periods. The dust and debris act as an umbrella and protect the microorganisms from direct contact with the vapor. For the fogging to work well, the rooms should be subject to both a Superclean as well as a good Spring cleaning; it is not possible to over-emphasize the importance of a thorough cleaning of the room prior to the fogging process. Likewise, if the vapor cannot penetrate into a particular place then no sanitization will occur. As a result, any unnecessary items should be disposed of and drawers and cabinets cleared. Where items cannot be removed, they should be raised to ensure an air path is available around and underneath them. Equipment such as safety cabinets, fume hoods, laminar flow benches, incubators, freezers, refrigerators, or other equipment will need to be identified to ensure that provisions are made so adequate vapor reaches these areas.</p>

<p>In summary, a thorough cleaning must be performed prior to any fogging actions. Fogging is no substitute for cleaning. If there is gross contamination – e.g. dust or covered surfaces – then the efficacy of the fogging will be severely reduced. It should also be noted that certain bacteria can remain viable in dust for long periods. Hence, for the sanitization to work well, the rooms should be subject to a Super and Spring clean. It is not possible to over-emphasize the importance of thorough cleaning of the room prior to the fogging process.</p>
